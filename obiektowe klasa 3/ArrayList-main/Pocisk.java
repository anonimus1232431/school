public class Pocisk {
    int nrSeryjny;
    double dlugosc;
    float szerokosc;
    float predkosc;
    float masa;

    public Pocisk(int nrSeryjny, double dlugosc, float szerokosc, float masa, float predkosc) {
        this.nrSeryjny = nrSeryjny;
        this.dlugosc = dlugosc;
        this.szerokosc = szerokosc;
        this.masa = masa;
        this.predkosc = predkosc;
    }
}
